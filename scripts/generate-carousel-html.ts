/**
 * Generate Carousel HTML Template
 * 
 * Creates a modern, gradient-based HTML carousel template for social media posts.
 * Designed for Instagram-style 1080x1080px posts.
 * 
 * Usage:
 *   npx ts-node scripts/generate-carousel-html.ts --topic "AI Tips" --slides 5
 *   npx ts-node scripts/generate-carousel-html.ts --topic "Tech News" --interactive
 */

interface SlideData {
  title?: string
  text?: string
  list?: string[]
  image?: string
  background?: string
}

interface CarouselOptions {
  topic: string
  slides: SlideData[]
  width?: number
  height?: number
  outputPath?: string
}

// Gradient presets
const GRADIENTS = [
  'linear-gradient(135deg, #667eea 0%, #764ba2 100%)', // Purple
  'linear-gradient(135deg, #f093fb 0%, #f5576c 100%)', // Pink
  'linear-gradient(135deg, #4facfe 0%, #00f2fe 100%)', // Blue
  'linear-gradient(135deg, #43e97b 0%, #38f9d7 100%)', // Green
  'linear-gradient(135deg, #fa709a 0%, #fee140 100%)', // Sunset
  'linear-gradient(135deg, #a8edea 0%, #fed6e3 100%)', // Pastel
  'linear-gradient(135deg, #ff9a9e 0%, #fecfef 100%)', // Rose
  'linear-gradient(135deg, #ffecd2 0%, #fcb69f 100%)', // Peach
]

// Color palettes
const COLORS = [
  { bg: '#667eea', text: '#ffffff' },
  { bg: '#f5576c', text: '#ffffff' },
  { bg: '#4facfe', text: '#ffffff' },
  { bg: '#43e97b', text: '#0a0a0a' },
  { bg: '#fa709a', text: '#ffffff' },
  { bg: '#fee140', text: '#0a0a0a' },
]

/**
 * Generate slides HTML from slide data
 */
function generateSlidesHtml(slides: SlideData[]): string {
  return slides.map((slide, index) => {
    const gradient = slide.background || GRADIENTS[index % GRADIENTS.length]
    
    return `
    <div class="slide" data-slide="${index + 1}">
      <div class="slide-bg" style="background: ${gradient}"></div>
      <div class="slide-badge">${index + 1}/${slides.length}</div>
      <div class="slide-content">
        ${slide.title ? `<h2 class="slide-title">${slide.title}</h2>` : ''}
        ${slide.text ? `<p class="slide-text">${slide.text}</p>` : ''}
        ${slide.list && slide.list.length > 0 ? `
          <ul class="slide-list">
            ${slide.list.map(item => `<li>${item}</li>`).join('')}
          </ul>
        ` : ''}
      </div>
      ${slide.image ? `<div class="slide-image" style="background-image: url('${slide.image}')"></div>` : ''}
      <div class="slide-footer">Generated by Merry Dashboard</div>
    </div>
    `
  }).join('')
}

