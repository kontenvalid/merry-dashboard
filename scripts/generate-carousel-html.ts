/**
 * Generate default slides for a topic
 */
export function generateDefaultSlides(topic: string, count = 5): SlideData[] {
  const templates: SlideData[] = [
    {
      title: topic,
      text: 'The complete guide you need',
      background: GRADIENTS[0]
    },
    {
      title: 'Why This Matters',
      text: 'Understanding the fundamentals will change everything',
      background: GRADIENTS[1]
    },
    {
      title: 'Key Insights',
      list: [
        'First important point',
        'Second crucial detail',
        'Third major takeaway'
      ],
      background: GRADIENTS[2]
    },
    {
      title: 'How to Get Started',
      list: [
        'Step 1: Begin here',
        'Step 2: Build momentum',
        'Step 3: See results'
      ],
      background: GRADIENTS[3]
    },
    {
      title: topic,
      text: 'Start your journey today! 🚀',
      background: GRADIENTS[4]
    }
  ]

  return templates.slice(0, count)
}

// CLI interface
if (require.main === module) {
  const args = process.argv.slice(2)
  
  // Parse arguments
  let topic = 'Social Media Post'
  let slideCount = 5
  let outputPath: string | undefined

  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--topic' && args[i + 1]) {
      topic = args[i + 1]
      i++
    } else if (args[i] === '--slides' && args[i + 1]) {
      slideCount = parseInt(args[i + 1]) || 5
      i++
    } else if (args[i] === '--output' && args[i + 1]) {
      outputPath = args[i + 1]
      i++
    } else if (args[i] === '--help' || args[i] === '-h') {
      console.log(`
Carousel HTML Generator

Usage:
  npx ts-node scripts/generate-carousel-html.ts [options]

Options:
  --topic <text>     Set the main topic/title (default: "Social Media Post")
  --slides <number>  Number of slides to generate (default: 5)
  --output <path>    Save to file path instead of stdout
  --help, -h         Show this help message

Examples:
  npx ts-node scripts/generate-carousel-html.ts --topic "AI Tips" --slides 4
  npx ts-node scripts/generate-carousel-html.ts --output carousel.html
      `)
      process.exit(0)
    }
  }

  // Generate carousel
  const slides = generateDefaultSlides(topic, slideCount)
  const html = generateCarouselHtml({ topic, slides })

  if (outputPath) {
    const fs = require('fs')
    fs.writeFileSync(outputPath, html)
    console.log(`✅ Carousel saved to: ${outputPath}`)
    console.log(`   Slides: ${slideCount}`)
    console.log(`   Topic: ${topic}`)
  } else {
    console.log(html)
  }
}

export default generateCarouselHtml
